// Type สำหรับ TypeScript
export type ThaiProvince = typeof THAILAND_PROVINCES[number];

// จำนวนจังหวัดทั้งหมด
export const PROVINCE_COUNT = THAILAND_PROVINCES.length;

// Helper function: ตรวจสอบว่าเป็นจังหวัดที่ถูกต้องหรือไม่
export const isValidProvince = (province: string): province is ThaiProvince => {
  return THAILAND_PROVINCES.includes(province as ThaiProvince);
};

// Helper function: ค้นหาจังหวัด (case-insensitive)
export const searchProvince = (query: string): ThaiProvince[] => {
  const lowerQuery = query.toLowerCase();
  return THAILAND_PROVINCES.filter(province => 
    province.toLowerCase().includes(lowerQuery)
  );
};